// FunciÃ³n para cambiar idioma dinÃ¡micamente
function changeLanguage(lang) {
    if (!translations[lang]) {
        console.error(`Language ${lang} not found`);
        return;
    }
    
    const t = translations[lang];
    
    // Actualizar header
    document.querySelector('h1').textContent = t.title;
    document.querySelector('.subtitle').textContent = t.subtitle;
    
    // Actualizar perfil
    document.querySelector('.profile-name').textContent = t.profile_name;
    document.querySelector('.profile-title').textContent = t.profile_title;
    document.querySelector('.profile-description').textContent = t.profile_description;
    
    // Actualizar servicios
    const serviceCards = document.querySelectorAll('.service-card');
    const serviceTypes = ['spanish', 'english', 'homework'];
    
    serviceCards.forEach((card, index) => {
        const serviceType = serviceTypes[index];
        const service = t.services[serviceType];
        
        if (service) {
            card.querySelector('.service-title').textContent = service.title;
            card.querySelector('.service-description').textContent = service.description;
            card.querySelector('.cta-button').textContent = service.cta;
            
            const featuresList = card.querySelector('.service-features');
            featuresList.innerHTML = '';
            service.features.forEach(feature => {
                const li = document.createElement('li');
                li.textContent = feature;
                featuresList.appendChild(li);
            });
        }
    });
    
    // Actualizar secciÃ³n about
    document.querySelector('.about-section h2').textContent = t.about_title;
    const aboutItems = document.querySelectorAll('.about-item');
    t.about_items.forEach((item, index) => {
        if (aboutItems[index]) {
            aboutItems[index].querySelector('h3').textContent = item.title;
            aboutItems[index].querySelector('p').textContent = item.description;
        }
    });
    
    // Actualizar contacto
    document.querySelector('.contact-section h2').textContent = t.contact_title;
    document.querySelector('.contact-section p').textContent = t.contact_subtitle;
    
    const contactItems = document.querySelectorAll('.contact-item');
    const contactKeys = ['email', 'whatsapp', 'location', 'hours'];
    
    contactKeys.forEach((key, index) => {
        if (contactItems[index]) {
            contactItems[index].querySelector('h4').textContent = t.contact_info[key].label;
            contactItems[index].querySelector('p').textContent = t.contact_info[key].value;
        }
    });
}

